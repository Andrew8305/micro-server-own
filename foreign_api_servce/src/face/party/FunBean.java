package face.party;

public class FunBean {
	
	private Long functionId;
	private String name;
	private String note;
	private Long config;
	private Long dicconfig;
	private Integer biztypId;
	public Long getFunctionId() {
		return functionId;
	}
	public void setFunctionId(Long functionId) {
		this.functionId = functionId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public Long getConfig() {
		return config;
	}
	public void setConfig(Long config) {
		this.config = config;
	}
	public Long getDicconfig() {
		return dicconfig;
	}
	public void setDicconfig(Long dicconfig) {
		this.dicconfig = dicconfig;
	}
	public Integer getBiztypId() {
		return biztypId;
	}
	public void setBiztypId(Integer biztypId) {
		this.biztypId = biztypId;
	}
	
	
	
}
